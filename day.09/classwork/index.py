# 1
# სამი ცვლადის შექმნა
int_var = 10        # integer ტიპის ცვლადი
float_var = 20.5    # float ტიპის ცვლადი
string_var = "30"   # string ტიპის ცვლადი

# ფორმატის შეცვლა და გამოყვანა
int_to_string = str(int_var)         # integer -> string
string_to_int = int(string_var)      # string -> integer
int_to_float = float(int_var)        # integer -> float

print(f"Integer to String: {int_to_string}")
print(f"String to Integer: {string_to_int}")
print(f"Integer to Float: {int_to_float}")

