# მესამე საკლასო დავალება
def calculator(num1,num2,operator):
    if operator == '+':
        print(num1 + num2)
    elif operator == '-':
        print(num1 - num2)
    elif operator == '*':
        print(num1 * num2)
    elif operator == '/':
        print(num1 / num2)
calculator(5,5,"+")       


